#include<iostream>
#include<algorithm>
#include<vector>

using namespace std;
int n;
int max_val = 0;

int main(){
    cin >> n;
    vector<int> ropes(n);

    for(int i = 0; i < n; ++i){
        cin >> ropes[i];
    }

    sort(ropes.begin(),ropes.end());

    for(int i = 0; i < n; ++i){
        max_val = max(max_val, ropes[i] * (n - i));
    }

    cout << max_val;
    return 0;
}